	end_at      DATETIME NOT NULL DEFAULT 0,
	begin_str   VARCHAR(50)  NOT NULL DEFAULT 0,
	end_str     VARCHAR(50)  NOT NULL DEFAULT 0,
	KEY agency_id(agency_id)
