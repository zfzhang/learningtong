DROP TABLE IF EXISTS tops_students;
CREATE TABLE tops_students
(
	id INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
	top_id     INT NOT NULL DEFAULT 0,
	student_id INT NOT NULL DEFAULT 0,
	avatar     VARCHAR(255) NOT NULL DEFAULT '',
