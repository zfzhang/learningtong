			<div class="detailTitle">对抗帕金森：英特尔涉足医疗大数据</div>
			<div class="detailSub">
				<span>来源：网络</span>
				<span>日期：11月06日 10:39</span>
			</div>
			<div class="detailCont">
				<p style="text-indent:2em;color:#676666;font-size:16px;line-height:24px;">据国外媒体报道，以处理器等硬件产品为主的英特尔一直从事着一项与互联网医疗相关的试验与工作。通过英特尔的可穿戴设备，收集人体大脑中与神经衰退性疾病，又被称作帕金森综合症患者的数据，并对此进行整理与分析。使用可穿戴设备对疑难杂症患者的数据进行收集，无论对于患者还是互联网医疗的大数据价值都将产生重要影响。</p>
				<p style="text-indent:2em;color:#676666;font-size:16px;line-height:24px;">英特尔大数据解决方案副总裁Ron Kasabian在接受媒体采访时表示“我们一开始与50名患者进行试验，确保技术及分析的准确性，目前则扩大至更广泛的临床测试范围”。他还指出，到明年年底，该项目将实现10000例的监测，能够帮助医疗研究人员更好地对抗帕金森综合症。目前，全球大概有500万人身患此症，患者会遭受运动迟缓、冻结、震颤的现象，十分痛苦。</p>
				<p style="text-indent:2em;color:#676666;font-size:16px;line-height:24px;">当然，任何事情都不是无独有偶。英特尔之所以进入此领域的一个原因是其创始人、前首席执行官Andy·Grove在2000年被诊断出帕金森综合症，不久后就辞去了CEO职务。Grove拥有传奇一样的人生，早先艰难存活，后来到美国餐馆打工，几年后考入了加州大学伯克利分校成为化学工程博士，最后建立英特尔，并将其打造成芯片巨头。当他被诊断出患有前列腺癌后，Grove就开始致力于运用各种资源来帮助医疗机构研究疾病，即便是在2000年患上帕金森综合症后也依然如此。</p>
				<p style="text-indent:2em;color:#676666;font-size:16px;line-height:24px;">在实际产品的规划中，英特尔开发了一种配有加速度计的可穿戴设备。在临床试验中，英特尔和医疗研发人员选择了现成的智能手表作为数据收集设备，24小时不间断收集患者的运动、震颤和睡眠数据，每天每个患者的数据量高达1GB。在接下来的一年内，研究人员还将通过心电图、血压、心率监测来获得更多数据，从而找到病情发展中的共性、并进行更有效的治疗。</p>
				<p style="text-indent:2em;color:#676666;font-size:16px;line-height:24px;">来自迈克尔·J·福克斯基金会的CEO托德·希勒对此评价说：“从1817年詹姆斯·帕金森医生首次描述这种病症到现在，我们与200年前医生评测这种病的方法基本上没多大区别。但随着英特尔研究的可穿戴设备的出现，这种情况或许很快就将发生改变了。”</p>
